package com.example.demo.entities;

public class Stores {

    private Long id_store;
    private String name_store;
    private String address_store;
    private String city_store;

    // Getters
    public Long getId_store() { return id_store; }
    public String getName_store() { return name_store; }
    public String getAddress_store() { return address_store; }
    public String getCity_store() { return city_store; }

    // Setters
    public void setId_store(Long id_store) { this.id_store = id_store;}
    public void setName_store(String name_store) {this.name_store = name_store;}
    public void setAddress_store(String address_store) {this.address_store = address_store;}
    public void setCity_store(String city_store) {this.city_store = city_store;}


}
